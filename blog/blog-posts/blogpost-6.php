			<h3>Goal Setting Performance Tools:</h3>
			<ul>
				<li>Know where you are going. Identify your desired end state/goal.</li>
				<li>Develop smaller, intermediate goals to keep you on track.</li>
				<li>Include physical and mental training as equal priorities.</li>
			</ul>
			<h3>Utilize the <strong>SMARTER</strong> goal setting method to help keep yourself on point and adapt to life’s ever changing demands.</h3>
			<ul>
				<li><strong>Specific</strong> – What do I need to DO? How do I need to BE?</li>
				<li><strong>Measurable</strong> – How fast? How far? Quantify to hold yourself accountable.</li>
				<li><strong>Action Oriented</strong> – Use action verbs and present tense.</li>
				<li><strong>Realistic</strong> – Set challenging but achievable goals.</li>
				<li><strong>Time Bound</strong> – Decide on a desired end date for achieving your goal.</li>
				<li><strong>Evaluate</strong>Evaluate – Am I on track for accomplishing this goal? Did I accomplish the goal?</li>
				<li><strong>Rewrite</strong> – Adapt and revise goals as needed.</li>
				<li><strong>Write your goals</strong> down and place them in a highly visible place to be reminded of them daily.</li>
				<li><strong>Share your goals</strong> with family and friends to assist with accountability and support.</li>
				<li><strong>Identify daily goals</strong> to maintain focus and motivation in training. Set reminders on phone or computer to go off at strategic times regarding daily goals.</li>
			</ul>
